int	ft_is_prime(int nb)
{
	int i;

	i = 3;
	if (nb == 0 || nb == 1)
		return (0);
	if (nb == 2)
		return (1);
	if (nb % 2 == 0)
		return (0);
	while (i <= nb)
	{
		if ((nb % i == 0) && (nb == i))
			return (1);
		i++;
	}
	return (0);
}
